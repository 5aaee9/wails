<script lang="ts">
  import {Greet} from "../bindings/main/GreetService.js";
  import {Events} from "@wailsio/runtime";

  let name: string = '';
  let result: string = 'Please enter your name below ðŸ‘‡';
  let time: string = 'Listening for Time event...';

  const doGreet = (): void => {
    if (!name) {
      name = 'from Go';
    }
    Greet(name).then((resultValue: string) => {
      result = resultValue;
    }).catch((err: any) => {
      console.log(err);
    });
  }

  Events.On('time', (timeValue: any) => {
    time = timeValue.data;
  });
</script>

<div class="container">
  <div>
    <span wml-openURL="https://wails.io">
      <img src="/wails.png" class="logo" alt="Wails logo"/>
    </span>
    <span wml-openURL="https://svelte.dev">
      <img src="/svelte.svg" class="logo svelte" alt="Svelte logo"/>
    </span>
  </div>
  <h1>Wails + Svelte</h1>
  <div class="card">
    <div class="result">{result}</div>
    <div class="input-box">
      <input class="input" bind:value={name} type="text" autocomplete="off"/>
      <button class="btn" on:click={doGreet}>Greet</button>
    </div>
  </div>
  <div class="footer">
    <div><p>Click on the Wails logo to learn more</p></div>
    <div><p>{time}</p></div>
  </div>
</div>

